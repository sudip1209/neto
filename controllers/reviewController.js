const factory = require("./handlerFactory");

exports.getAllReview = catchAsync(async (req, res, next) => {
  let filter = {};
  if (req.params.tourId) filter = { tour: req.params.tourId };
  const review = await Review.find(filter);
  res.status(200).json({
    status: "success",
    result: {
      review,
    },
  });
});

